// O fatorial é a multiplicação de um número natual pelos seus antecessores, exceto o zero. Por exemplo:
// O fatorial é representado pelo sinal (!)
// 4! = 4 x 3 x 2 x 1 = 24
// Com base nesses informaçõoes, crie um algoritimo que o fatorial de 10.
let fatorial = 10;

for (let index = 10; index > 0; index -= 1) {
    fatorial = fatorial * index;
}
